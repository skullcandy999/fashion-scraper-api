    """
    Convert our SKU to full DSQUARED2 original code.

    Args:
        our_sku: Our format (e.g., "DQS71AN0669-470")

    Returns:
        Full original code (e.g., "S71AN0669D30038470") or None if not in lookup
    """
    our_sku = our_sku.strip().upper()

    if our_sku in DSQUARED2_LOOKUP:
        return DSQUARED2_LOOKUP[our_sku]

    return None


